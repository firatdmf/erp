added = 0
updated = 0
for k, v in tr.items():
    existing = next((e for e in po if e.msgid == k), None)
    if existing:
        if existing.msgstr != v:
            existing.msgstr = v
            updated += 1
    else:
        po.append(polib.POEntry(msgid=k, msgstr=v))
        added += 1

po.save("locale/tr/LC_MESSAGES/django.po")
po.save_as_mofile("locale/tr/LC_MESSAGES/django.mo")
